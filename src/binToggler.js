class BinaryToggler {
  constructor(rules) {
    this.rules = rules;
    const map = Object.fromEntries(
      this.rules.map(key => [key, false])
    );
    this.map = map;
  }

  getAllRules() {
    return this.rules;
  }

  getRuleCode(index) {
    return this.rules[index];
  }

  getRuleState(code) {
    return this.map[code];
  }

  toggleRuleState(code) {
    this.map[code] = !this.map[code];
  }

  toggleRuleStateIndex(low, high) {
    for (let i = low; i <= high; i++) {
      this.toggleRuleState(this.rules[i]);
    }
  }

  getRulesCode() {
    const flags = this.rules.map(key => this.map[key] ? 1 : 0);

    // Pack into bytes
    const bytes = new Uint8Array(Math.ceil(flags.length / 8));

    flags.forEach((flag, i) => {
        if (flag) {
            bytes[Math.floor(i / 8)] |= 1 << (i % 8);
        }
    });

    // Encode
    const encoded = btoa(String.fromCharCode(...bytes));
    return encoded;
  }

  setRulesFromCode(code) {
    const bytes = Uint8Array.from(
        atob(code),
        c => c.charCodeAt(0)
    );

    const count = this.rules.length;
    const flags = Array(count);

    for (let i = 0; i < count; i++) {
      flags[i] = !!(
        bytes[Math.floor(i / 8)] &
        (1 << (i % 8))
      );
      this.map[this.rules[i]] = flags[i];
    }
  }
}

